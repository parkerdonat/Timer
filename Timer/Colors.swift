import Foundation
import UIKit

extension UIColor {
    static func lightGreyColorTimer() -> UIColor {
        return UIColor(red: 0.231, green: 0.231, blue: 0.263, alpha: 1)
    }
    
    static func darkGreyColorTimer() -> UIColor {
        return UIColor(red: 0.137, green: 0.137, blue: 0.153, alpha: 1)
    }
    
    static func tanColorTimer() -> UIColor {
        return UIColor(red: 0.729, green: 0.737, blue: 0.702, alpha: 1)
    }
    
    static func greenColorTimer() -> UIColor {
        return UIColor(red: 0.361, green: 0.706, blue: 0.271, alpha: 1)
    }
    
    static func lightBlueColorTimer() -> UIColor {
        return UIColor(red: 0.361, green: 0.643, blue: 0.898, alpha: 1)
    }
}